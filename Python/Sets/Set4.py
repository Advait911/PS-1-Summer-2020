# Python program to demonstrate 
# Accessing of elements in a set 

# Creating a set 
set1 = set(["Python", "For", "Everyone"]) 
print("\nInitial set") 
print(set1) 

# Accessing element using 
# for loop 
print("\nElements of set: ") 
for i in set1: 
	print(i, end=" ") 

# Checking the element 
# using in keyword 
print("Python" in set1) 
